function applyLang(lang){
  document.documentElement.setAttribute("data-lang", lang);
  document.documentElement.lang = (lang === "de") ? "de" : "en";
  document.getElementById("langLabel").textContent = (lang === "de") ? "DE" : "EN";

  document.querySelectorAll("[data-i18n]").forEach(el => {
    const key = el.getAttribute("data-i18n");
    const value = i18n[lang]?.[key];
    if (value) el.textContent = value;
  });

  localStorage.setItem("freiraum_lang", lang);
}

const saved = localStorage.getItem("freiraum_lang");
applyLang(saved || "de");

document.getElementById("langBtn").addEventListener("click", () => {
  const current = document.documentElement.getAttribute("data-lang") || "de";
  applyLang(current === "de" ? "en" : "de");
});
// ===== Lightbox Funktion =====
document.addEventListener("DOMContentLoaded", function(){

  const lightbox = document.getElementById("lightbox");
  const lightboxContent = document.getElementById("lightboxContent");
  const lightboxClose = document.getElementById("lightboxClose");

  document.querySelectorAll(".lightbox-trigger").forEach(el => {
    el.addEventListener("click", function(e){
      e.preventDefault();

      const type = this.getAttribute("data-type");
      const src = this.getAttribute("href");

      lightbox.style.display = "flex";

      if(type === "image"){
        lightboxContent.innerHTML = `<img src="${src}">`;
      }

      if(type === "video"){
        lightboxContent.innerHTML = `<video src="${src}" controls autoplay></video>`;
      }
    });
  });

  lightboxClose.addEventListener("click", () => {
    lightbox.style.display = "none";
    lightboxContent.innerHTML = "";
  });

  lightbox.addEventListener("click", (e) => {
    if(e.target === lightbox){
      lightbox.style.display = "none";
      lightboxContent.innerHTML = "";
    }
  });

});